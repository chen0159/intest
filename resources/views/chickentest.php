    <p>
        The <b>chicken</b> (Gallus domesticus) is a domesticated junglefowl species, with attributes of wild species such as the grey and the Ceylon junglefowl[1]
        that are originally from Southeastern Asia.
        Rooster or cock is a term for an adult male bird, and a younger male may be called a cockerel.
        A male that has been castrated is a capon. An adult female bird is called a hen and a sexually immature female is called a pullet.
    </p>
    Originally raised for cockfighting or for special ceremonies, chickens were not kept for food until the Hellenistic period (4th–2nd centuries BC).
    [2][3] Humans now keep chickens primarily as a source of food (consuming both their meat and eggs) and as pets.

