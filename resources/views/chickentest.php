    <h2>General biology and habitat</h2>
    <p id="gbahone">
        Roosters can usually be differentiated from hens by their striking plumage of long flowing tails and shiny, pointed feathers on their necks ('hackles') and backs ('saddle'), which are typically of brighter,
        bolder colours than those of females of the same breed. However, in some breeds, such as the Sebright chicken, the rooster has only slightly pointed neck feathers,
        the same colour as the hen's. The identification can be made by looking at the comb, or eventually from the development of spurs on the male's legs (in a few breeds and in certain hybrids,
        the male and female chicks may be differentiated by colour). Adult chickens have a fleshy crest on their heads called a comb, or cockscomb, and hanging flaps of skin either side under their beaks called wattles.
        Collectively, these and other fleshy protuberances on the head and throat are called caruncles. Both the adult male and female have wattles and combs, but in most breeds these are more prominent in males.
        A 'muff' or 'beard' is a mutation found in several chicken breeds which causes extra feathering under the chicken's face, giving the appearance of a beard.[33]
    </p>
    <h3>Vocalizations</h3>
    <p>
        When a rooster finds food, he may call other chickens to eat first. He does this by clucking in a high pitch as well as picking up and dropping the food.
        This behaviour may also be observed in mother hens to call their chicks and encourage them to eat.
    </p>
